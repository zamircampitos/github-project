def add_numbers(a: int, b: int) -> int:
    """
    Adds two numbers.
    
    Args:
        a (int): The first number.
        b (int): The second number.
        
    Returns:
        int: The sum of the two numbers.
    """
    return a + b

if __name__ == "__main__":
    result = add_numbers(5, 3)
    print(result)
